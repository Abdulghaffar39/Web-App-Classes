// >>>>>>>>>>>>>>>>>> Type of Network Topologies (Machine ki Example) <<<<<<<<<<<<<<<<<<<<

// ===================================== Bus Topology ====================================
// (Pros: Simple aur cheap. Sabhi computers ek single cable (bus) se connected hote hain)

// ===================================== Star Topology  ====================================
// (Har computer ek central device (hub/switch/server) se connected hota hai. Communication only central device)

// ===================================== Ring Topology  ====================================
// (Har computer apne doosre do computers se connect hota hai, aur ek ring (circle) banata hai. Easy to install aur predictable performance)

// ===================================== Mesh Topology  ====================================
// (Har computer network me har dusre computer se direct connected hota hai. Bohot zyada wiring lagti hai, costly hota hai.)

// ===================================== Tree Topology  ====================================
// (is me do PC connect hote hai. chote setup ke liya hota hai)

// ==================================== Hybird Topology  ===================================
// (Do ya zyada topologies ka combination hota hai (jaise Star + Ring, ya Star + Mesh).)



